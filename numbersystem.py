#Number system in Python 
#Binary - 0,1 (Base 2)
# Decimal - Range from 0-9 (Base 10)
# Octal - Range from 0-7 (Base 8)
# Hexadecimal - Range from 0 - 16 (Base 16)

#Octal to Decimal Conversion 
Octal=0o127
print(Octal)

#Hexadecimal to Decimal conversion
Hexadecimal = 0X2A
print(Hexadecimal)

#Binary to decimal 
Binary = 0B1011
print(Binary)

#Decimal to binary 
print(bin(13))

#Decimal to octal 
print(oct(13))

print(hex(36))

print(oct(13)+hex(14))